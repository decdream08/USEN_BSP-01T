void TAS3251_DSP_Amp_Init(uint8_t I2C_ADR)
{
	uint32_t uSize = 0, i;
	uint8_t Data = 0;
	
#ifdef _I2C_DEBUG_MSG
	_DBG("\n\rTAS3251_DSP_Amp_Init");
#endif
	uSize = sizeof(TAS3251_Program_Memory);
	
	//Write TAS3251_Program_Memory
	for(i =0;i<uSize;i++)
	{
		if(i%2 == 0)
			I2C_Interrupt_Write_Data(I2C_ADR, TAS3251_Program_Memory[i],&(TAS3251_Program_Memory[i+1]),1);
	}

	//Write TAS3251_Init
	if(I2C_ADR == TAS3251_DEVICE_ADDR_H) //TAS3251_DEVICE_ADDR_H						(0x4B) //8bit - 0x96
	{
		uSize = sizeof(TAS3251_ADDR_H_Init)/2;

		for(i =0;i<uSize;i++)
		{
			Data = TAS3251_ADDR_H_Init[i][1];
			I2C_Interrupt_Write_Data(I2C_ADR, TAS3251_ADDR_H_Init[i][0],&Data,1);
		}
	}
	else	//TAS3251_DEVICE_ADDR_L						(0x4A) //8bit - 0x94
	{
		uSize = sizeof(TAS3251_ADDR_L_Init)/2;

		for(i =0;i<uSize;i++)
		{
			Data = TAS3251_ADDR_L_Init[i][1];
			I2C_Interrupt_Write_Data(I2C_ADR, TAS3251_ADDR_L_Init[i][0],&Data,1);
		}

	}

#if 0 //Disable by LGD's request
	//Add default volume setting....
	Data = TAS3251_Volume_Table[2];
	I2C_Interrupt_Write_Data(I2C_ADR, TAS3251_LEFT_VOL_CONTROL_REG,&Data ,1);
	I2C_Interrupt_Write_Data(I2C_ADR, TAS3251_RIGHT_VOL_CONTROL_REG,&Data ,1);
#endif

#ifdef _I2C_DEBUG_MSG
	_DBG("\n\rTAS3251_DSP_Amp_Init - OK !!!");
#endif
}

void TAS3251_DSP_Amp_Mute(TAS3251_ADDR I2C_ADR, Bool Mute_On) // First of all, You need to make sure if current page is Book 00 & Page00
{
	uint8_t uRead = 0;

#ifdef _I2C_DEBUG_MSG	
	_DBG("\n\rTAS3251_DSP_Amp_Mute!!!");
#endif

	I2C_Interrupt_Read_Data(I2C_ADR, TAS3251_MUTE_CONTROL_REG,&uRead,1); //Read Current mute status

	if(Mute_On == TRUE) // Mute Off
	{ //Set Mute Off
		uRead &= TAS3251_MUTE_OFF;
		I2C_Interrupt_Write_Data(I2C_ADR, TAS3251_MUTE_CONTROL_REG,&uRead,1);
		_DBG("\n\rMute Off");
	}		
	else // Mute On
	{ //Set Mute On
		uRead |= TAS3251_MUTE_ON;
		I2C_Interrupt_Write_Data(I2C_ADR, TAS3251_MUTE_CONTROL_REG,&uRead,1);
		_DBG("\n\rMute On");
	}
}

