void TAS3251_DSP_Amp_Volume_Control(Vol_Setting Vol_mode, TAS3251_ADDR I2C_ADR)
{
	uint8_t uRead = 0, uCurVolLevel = 0, i;
	
#ifdef _I2C_DEBUG_MSG	
	_DBG("\n\rTAS3251_DSP_Amp_Volume_Control!!! --- BVolumeUp =");
#endif

	//We suppose the right level and the left level is same. Read Current mute status
	I2C_Interrupt_Read_Data(I2C_ADR, TAS3251_LEFT_VOL_CONTROL_REG,&uRead,1);
	
	// Check whether current volume level is Max/Min or not
	if(((uRead >= TAS3251_Volume_Table[MIN_VOLUME_LEVEL]) && (Vol_mode == Volume_Down))	\
		|| ((uRead <= TAS3251_Volume_Table[MAX_VOLUME_LEVEL]) && (Vol_mode == Volume_Up)))
	{
#ifdef _I2C_DEBUG_MSG
		_DBG("\n\rCan't change current volume level becuase it's a MAX or MIN !!! ");
		_DBH(uRead);
#endif
		return;
	}

	for(i =0; i < VOLUME_LEVEL_NUMER; i++)
	{
		if(TAS3251_Volume_Table[i] == uRead)
		{
			uCurVolLevel = i;
#ifdef _I2C_DEBUG_MSG
			_DBG("\n\ruCurVolLevel : ");
			_DBH(uCurVolLevel);
#endif
			break;
		}
	}

	if(Vol_mode == Volume_Up) //Volume up setting -A increasing number is smaller volume
		uCurVolLevel -= 1;
	else //Volume down setting
		uCurVolLevel += 1;
			
	uRead = TAS3251_Volume_Table[uCurVolLevel];

	I2C_Interrupt_Write_Data(I2C_ADR, TAS3251_LEFT_VOL_CONTROL_REG,&uRead,1);
	I2C_Interrupt_Write_Data(I2C_ADR, TAS3251_RIGHT_VOL_CONTROL_REG,&uRead,1);
}

